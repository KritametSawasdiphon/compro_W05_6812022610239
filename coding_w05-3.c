#include <stdio.h> // เรียกใช้ไลบรารีมาตรฐานสำหรับ input/output

int main() {
    // ประกาศตัวแปรแบบทศนิยมเพื่อเก็บค่าที่ต้องการแสดงผล
    float a = 0.617;
    int b = 25;
    float c = 19.000;
    int d = 9;
    float e = 124.555;
    float f = 0.001;
    // แสดงค่าของตัวแปร a พร้อมแสดงทศนิยม 3 ตำแหน่งและเครื่องหมาย %
    printf("a = %.3f %%\n", a);
    // แสดงค่าของตัวแปร b (จำนวนเต็ม) พร้อมเครื่องหมาย %
    printf("b = %d %%\n", b);
    // แสดงค่าของตัวแปร c ทศนิยม 3 ตำแหน่งและเครื่องหมาย %
    printf("c = %.3f %%\n", c);
    // แสดงค่าของตัวแปร d (จำนวนเต็ม) พร้อมเครื่องหมาย %
    printf("d = %d %%\n", d);
    // แสดงค่าของตัวแปร e ทศนิยม 3 ตำแหน่งและเครื่องหมาย %
    printf("e = %.3f %%\n", e);
    // แสดงค่าของตัวแปร f ทศนิยม 3 ตำแหน่งและเครื่องหมาย %
    printf("f = %.3f %%\n", f);
    return 0; // จบ
}